export function fmtNum(value: number): string {
  if (value >= 1_000_000) {
    return `${(value / 1_000_000).toFixed(1)}M`;
  }
  if (value >= 10_000) {
    return `${(value / 1_000).toFixed(1)}k`;
  }
  return value.toLocaleString();
}

export function fmtPct(value: number, signed = true): string {
  const sign = signed && value > 0 ? "+" : "";
  return `${sign}${(value * 100).toFixed(1)}%`;
}

export function fmtAgo(minutes: number | null): string {
  if (minutes == null) {
    return "-";
  }
  if (minutes < 60) {
    return `${minutes}분 전`;
  }
  const hours = Math.floor(minutes / 60);
  const remain = minutes % 60;
  return remain ? `${hours}시간 ${remain}분 전` : `${hours}시간 전`;
}

export function StatusChip({ tone, children }: PropsWithChildren<{ tone: "up" | "down" | "warn" | "info" | "muted" | "spike" }>) {
  return <span className={`chip ${tone}`}>{children}</span>;
}

export function LoadingState({ label }: { label: string }) {
  return (
    <div className="empty">
      <div className="loader" />
      <div style={{ marginTop: 12 }}>{label}</div>
    </div>
  );
}

export function EmptyState({ title, body, action }: { title: string; body: string; action?: ReactNode }) {
  return (
    <div className="empty">
      <div className="big">{title}</div>
      <div>{body}</div>
      {action ? <div style={{ marginTop: 12 }}>{action}</div> : null}
    </div>
  );
}
